package kiku.practice;

public class JavaToJson {
    String wordDirection;
    String numOfFirstWords;
    String numOfLastWords;
    String type;
    String path;
    String summarize;
    String firstLetterOnly;
    String incrementalPositions;

    public JavaToJson() {
    }

    public String getWordDirection() {
        return wordDirection;
    }

    public void setWordDirection(String wordDirection) {
        this.wordDirection = wordDirection;
    }

    public String getNumOfFirstWords() {
        return numOfFirstWords;
    }

    public void setNumOfFirstWords(String numOfFirstWords) {
        this.numOfFirstWords = numOfFirstWords;
    }

    public String getNumOfLastWords() {
        return numOfLastWords;
    }

    public void setNumOfLastWords(String numOfLastWords) {
        this.numOfLastWords = numOfLastWords;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }

    public String getSummarize() {
        return summarize;
    }

    public void setSummarize(String summarize) {
        this.summarize = summarize;
    }

    public String getFirstLetterOnly() {
        return firstLetterOnly;
    }

    public void setFirstLetterOnly(String firstLetterOnly) {
        this.firstLetterOnly = firstLetterOnly;
    }

    public String getIncrementalPositions() {
        return incrementalPositions;
    }

    public void setIncrementalPositions(String incrementalPositions) {
        this.incrementalPositions = incrementalPositions;
    }
}
